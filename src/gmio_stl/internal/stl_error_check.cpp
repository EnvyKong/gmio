#include "stl_error_check.h"

#include "../../gmio_core/error.h"
#include "../stl_error.h"
#include "../stl_io.h"

namespace gmio {

bool STL_binaryCheckByteOrder(Endianness byte_order)
{
    return byte_order == Endianness_Little || byte_order == Endianness_Big;
}

bool STL_asciiCheckFloatPrecision(uint8_t prec)
{
    return 0 < prec && prec <= 9;
}

} // namespace gmio
